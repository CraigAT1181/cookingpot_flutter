class User {
  final String userId;
  final String authId;
  final String userName;
  final String email;
  final String profilePic;
  final String town;
  final String allotment;
  final String plot;

  User({
    required this.userId,
    required this.authId,
    required this.userName,
    required this.email,
    required this.profilePic,
    required this.town,
    required this.allotment,
    required this.plot,
  });
}
